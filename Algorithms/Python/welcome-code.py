# Welcome message for Hacktoberfest contributors
def welcome_contributor(username):
    print(f"Welcome, {username}!")
    print("Thank you for contributing to Hacktoberfest.")
    print("Happy coding!")

# List of contributors
contributors = ["Alice", "Bob", "Charlie", "David"]

# Welcome each contributor
for contributor in contributors:
    welcome_contributor(contributor)
    print("\n")  # Add a newline for separation
